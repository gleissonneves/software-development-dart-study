void main() {
  // Soma
  var soma = 5 + 5;
  print('o resultado de soma é ${soma}');

  // Divisão
  var divisao = 10 / 2;
  print('o resultado de divisão é ${divisao}');

  // Subtração
  var subtracao = 10 - 5;
  print('o resultado de subtração é ${subtracao}');

  // Mutiplicação
  var mutiplicacao = 2 * 5;
  print('o resultado de multiplicacao é ${mutiplicacao}');

  // Módulo (resto de divisão)
  var restoDivisao = 10 % 2;
  print('o resto de divisão(modulo) é ${restoDivisao}');

  // resto de divisão de inteiros
  var divisaoResultadosInt = 10 ~/ 3;
  print('o resto de divisão(modulo) é ${divisaoResultadosInt}');

  // seguindo as regras metemáticas
  var inversao = -(-10);
  print('-(-10) = ${inversao}');
}
